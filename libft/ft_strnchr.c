#include "libft.h"

char		ft_strnchr(char *s, char c, int n)
{
	int i;

	i = 0;
	if (!(s || c || n))
		return (0);
	while (i < n && s[i] != '\0')
	{
		if (s[i] == c)
			return (s[i]);
		i++;
	}
	return (0);
}